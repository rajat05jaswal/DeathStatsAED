    public void initData(){
        HashMap<String, ArrayList<PoliceMan>> hm=business.getHashPolice();
        ArrayList<PoliceMan> list = null;
        if(hm.containsKey(dp.getLocationOfDeath())){
          list=hm.get(dp.getLocationOfDeath());         
        }
        if(list!=null){
            DefaultTableModel model = (DefaultTableModel) policeJTable.getModel();
            model.setRowCount(0);
            for(PoliceMan p: list){
                Object[] row = new Object[1];
                row[0] = p;
                model.addRow(row);
            } 
        }
    }
