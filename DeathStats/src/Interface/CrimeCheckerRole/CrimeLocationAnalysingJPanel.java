    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        request.setStatus("Approved");
        request.setMessage("Approved but not analysed!!");
        container.remove(this);
        CardLayout layout = (CardLayout) container.getLayout();
        layout.previous(container);
        Component[] components = container.getComponents();
        for(Component c :components){
            if(c instanceof CrimeListsJPanel){
                CrimeListsJPanel crimeLists = (CrimeListsJPanel)c;
                crimeLists.populateData();
            }
        }
    }//GEN-LAST:event_backJButtonActionPerformed

    private void deploybtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deploybtnActionPerformed
        // TODO add your handling code here:
        request.setStatus("Approved");
        request.setMessage("Approved and Analysed !!");
        HashMap<String, ArrayList<PoliceMan>> hm=business.getHashPolice();
        String location =dp.getLocationOfDeath();
        if(hm.containsKey(location)){
            PoliceMan p=new PoliceMan();
            hm.get(location).add(p);
        }else{
            ArrayList<PoliceMan> list= new ArrayList<PoliceMan>();
            PoliceMan p=new PoliceMan();
            list.add(p);
            hm.put(location, list);
        }
        JOptionPane.showMessageDialog(null, "Policeman deployed successfully  !!!");
        initData();
    }//GEN-LAST:event_deploybtnActionPerformed


