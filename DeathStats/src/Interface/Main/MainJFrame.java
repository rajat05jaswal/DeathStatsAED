    private void PopulateDataInNewsFeed() throws FileNotFoundException, IOException{
        BufferedReader reader = null;
        File file = new File("./Death.csv");
        if(!file.exists())
            System.out.println("File not found at the path specified: "+"./Death.csv");
        try { 
            reader = new BufferedReader(new FileReader(file));
        } catch (FileNotFoundException ex) {
            return;
        }
        
        ArrayList<String[]> list=new ArrayList<>();
        String line="";
        while((line=reader.readLine()) !=null){
            String[] rows = line.split(",");
            list.add(rows);
        }
        list.remove(0);
        int totalRevenue=0;
        for(String[] row : list){
            totalRevenue+=Integer.parseInt(row[9]);
        }
        
        totalDeaths.setText(String.valueOf(list.size()));
        totalMoney.setText(String.valueOf(totalRevenue));
        String acc=accidentProneArea(list);
        accidentProne.setText(acc);
        String crime=crimeProneArea(list);
        crimeProne.setText(crime);
        populateMap(list);
    }
    
