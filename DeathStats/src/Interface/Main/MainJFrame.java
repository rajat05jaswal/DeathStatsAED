    private void generateDeathExcel(){
        File file = new File("./Death.csv");
        FileWriter writer=null;
        if(file.exists()){
            file.delete();
        }
        try {
            file.createNewFile();
        } catch (IOException ex) {
            System.out.println(ex);
        }
        System.out.println("New Death File Created");
        try {
            writer = new FileWriter(file);
            writer.append("DeathId, fName, lName, gender, age, monthlySalary, noofDependents, ReasonOfDeath, locationOfDeath, amountAssigned, generalReasonOfDeath");
            writer.append("\n");
        } catch (IOException ex) {
            System.out.println(ex);
        }
        
        for(DeadPerson dp:system.getDeadpeople().getDeadPeople()){
            try {
                String s="";
                s+=dp.toString()+",";
                s+=dp.getfName()+",";
                s+=dp.getlName()+",";
                s+=dp.getGender()+",";
                s+=dp.getAge()+",";
                s+=dp.getMonthlySalary()+",";
                s+=dp.getNoOfDependents()+",";
                s+=dp.getReasonOfDeath()+",";
                s+=dp.getLocationOfDeath()+",";
                s+=dp.getamountAssigned()+",";
                s+=dp.getGeneralReasonOfDeath()+",";
                writer.append(s);
                writer.append("\n");
            } catch (IOException ex) {
                System.out.println(ex);
            }
        }
        try {
            writer.close();
        } catch (IOException ex) {
            Logger.getLogger(MainJFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
