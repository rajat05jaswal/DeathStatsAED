    public void populateMap(ArrayList<String[]> list){
        String[] loc={"NorthStation","Longwood","SouthStation","Lechmere","Park Street","BackBay","TremontStreet","RoxBury"};
        ArrayList<String> locList= new ArrayList<>();
        for(String s:loc){
            locList.add(s);
        }
        Random r=new Random();
        for(Component jpanel : mapContainer.getComponents()){
            if(jpanel instanceof JPanel){
                ((JPanel) jpanel).removeAll();
            }
            
        }
        for(String[] row:list){
            String location=row[8];
            if(locList.contains(location)){
                JLabel label=new JLabel();
                label.setSize(20,40);
                label.setIcon(displayPicture("Icons/deadBody.png"));
                JPanel panel = null;
                switch(location){
                    case "NorthStation":
                        panel=northStationJPanel;
                        break;
                    case "Longwood":
                        panel=longwood;
                        break;
                    case "SouthStation":
                        panel=southStationJPanel;
                        break;
                    case "Lechmere":
                        panel=lechmere;
                        break;
                    case "Park Street":
                        panel=parkStreet;
                        break;
                    case "BackBay":
                        panel=backBay;
                        break;
                    case "TremontStreet":
                        panel=tremontStreet;
                        break;
                    case "RoxBury":
                        panel=roxburyJPanel;
                        break;  
                }
                
                int min=panel.getWidth();
                int max=panel.getHeight();
                int x=r.nextInt(min-20);
                int y=r.nextInt(Math.abs(max-40));
                label.setLocation(x, y);
                panel.add(label);
                
            }
        }
    }
    
    public String accidentProneArea(ArrayList<String[]> listFile){
         HashMap<String, ArrayList<String>> hm=new HashMap<>();
         for(String[] row: listFile){
            if(row[10].equalsIgnoreCase("accident")){
                String location=row[8];
                if(hm.containsKey(location)){
                    hm.get(location).add(row[0]);
                }else{
                    ArrayList<String> list= new ArrayList<>();
                    list.add(row[0]);
                    hm.put(location, list);
                }
            }
         }
         int max=0;
         String maxLocation="";
         for(Map.Entry<String, ArrayList<String>> entry: hm.entrySet()){
             if(entry.getValue().size()>max){
                 max=entry.getValue().size();
                 maxLocation=entry.getKey();
             }
         }
         return maxLocation;
    }
    
