    private void deathStatsTransferActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deathStatsTransferActionPerformed
        // TODO add your handling code here:
        requestMain.setMessage("Approved and sent for DeathStats analysis and research.");
        requestMain.setStatus("Approved");
        dp.setAmountDecided(true);
        
        DeathStatTransferWorkRequest req=new DeathStatTransferWorkRequest();
        req.setDeadPerson(dp);
        req.setSender(account);
        req.setMessage("Death Registerd for further analysis and Research");
        req.setStatus("Sent");
        
        Organization org = null;
        for(Network n: business.getNetworkList()){
            for(Enterprise e: n.getEnterpriseDirectory().getEnterpriseList()){
                if(e instanceof DeathStatsEnterprise){
                    for (Organization organization : e.getOrganizationDirectory().getOrganizationList()){
                        if (organization instanceof AnalysisOrganization){
                            org = organization;
                            break;
                        }
                    }
                }
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(req);
            account.getWorkQueue().getWorkRequestList().add(req);
        }
        DeathStatsStatusJPanel deathStatsStatusJPanel=new DeathStatsStatusJPanel(container,org,account,business);
        container.add("deathStatsStatusJPanel",deathStatsStatusJPanel);
        CardLayout layout=(CardLayout)container.getLayout();
        layout.next(container);

    }//GEN-LAST:event_deathStatsTransferActionPerformed

