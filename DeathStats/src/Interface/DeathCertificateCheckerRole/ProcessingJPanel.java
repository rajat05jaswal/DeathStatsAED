    private void populateData(){
        fname.setText(dp.getfName());
        lname.setText(dp.getlName());
        age.setText(String.valueOf(dp.getAge()));
        String gender= dp.getGender();
        if(gender =="M"){
            rbtnM.setSelected(true);
        }else{
            rbtnF.setSelected(true);
        }
        txtLocation.setText(dp.getLocationOfDeath());
        txtDependants.setText(String.valueOf(dp.getNoOfDependents()));
        txtReason.setText(dp.getReasonOfDeath());
        txtIncome.setText(String.valueOf(dp.getMonthlySalary()));
        if(dp.getNoOfDependents()>2){
            deathStatsTransfer.setEnabled(false);
        }
        else{
            gvtTransferBtn.setEnabled(false);
        }
    }
