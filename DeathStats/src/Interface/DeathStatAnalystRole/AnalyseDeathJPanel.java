    private void assignBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = deathJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Death.");
            return;
        }
        DeadPerson dp=(DeadPerson) deathJTable.getValueAt(selectedRow,3);
        if(dp.isAnalysisDone()){
            JOptionPane.showMessageDialog(null, "Analysis has been done on this case!!");
            return; 
        }
        dp.setAnalystAnalysing(account.getEmployee().getName());
        populateDeathTable();
    }//GEN-LAST:event_assignBtnActionPerformed

    private void analyseBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_analyseBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = deathJTable.getSelectedRow();
        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a Death.");
            return;
        }
        DeadPerson dp=(DeadPerson) deathJTable.getValueAt(selectedRow,3);
        if(dp.getAnalystAnalysing().equals("NA")){
            JOptionPane.showMessageDialog(null, "Kindly assign the death to you first !!");
            return;
        }
        if(dp.isAnalysisDone()){
            JOptionPane.showMessageDialog(null, "Analysis has been done on this case!!");
            return; 
        }
        if(!dp.getAnalystAnalysing().equals(account.getEmployee().getName()) && !dp.getAnalystAnalysing().equals("NA")){
            JOptionPane.showMessageDialog(null, "Another analyst analysing this death");
            return;
        }
        
        AnalysingCurrentDeathJPanel processingJPanel=new AnalysingCurrentDeathJPanel(container,organization,account,business,dp);
        container.add("analysingDeathJpanel",processingJPanel);
        CardLayout layout=(CardLayout)container.getLayout();
        layout.next(container);
    }//GEN-LAST:event_analyseBtnActionPerformed

