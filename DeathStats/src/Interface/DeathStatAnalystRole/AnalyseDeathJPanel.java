    public void populateDeathTable(){
        DefaultTableModel model = (DefaultTableModel) deathJTable.getModel();
        
        model.setRowCount(0);
        for (DeadPerson dp: business.getDeadpeople().getDeadPeople()){
            if(dp.isAmountDecided()){
                Object[] row = new Object[10];
                row[0] = dp.getfName();
                row[1] = dp.getlName();
                row[2] = dp.getAge();
                row[3] = dp;
                row[4] = dp.getLocationOfDeath();
                row[5] = dp.getNoOfDependents();
                row[6] = dp.getMonthlySalary();
                row[7] = dp.getReasonOfDeath();
                row[8] =dp.getAnalystAnalysing();
                String analysis="";
                if(dp.isAnalysisDone()){
                    analysis="Done";
                }else{
                    analysis="Pending";
                }
                row[9] = analysis;
                model.addRow(row);
            }            
        }
    }
